
public class CombiOp {

	public static void main(String[] args) {
		int numero = 25;
		
		numero+=5;
		System.out.println(numero);
		
		numero-=15;
		System.out.println(numero);
		
		numero*=2;
		System.out.println(numero);
		
		numero/=2;
		System.out.println(numero);
		
		numero%=3;
		System.out.println(numero);
	}

}
